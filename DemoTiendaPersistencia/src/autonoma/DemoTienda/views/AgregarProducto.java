    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        String nombre = this.txtNombre.getText();
        
        try {
            double precio = Double.parseDouble(this.txtPrecio.getText());
            Producto producto = new Producto(nombre, precio);
            if(this.tienda.agregarProducto(producto)){
                JOptionPane.showMessageDialog(this, "Producto "+nombre+" ha sido agregado exitosamente");
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Ha ocurrido un error");
                this.dispose();
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un n√∫mero en el campo precio");
            this.txtPrecio.setText("");
        }
        
        
        
        

    }                                          

